int	ft_str_is_alpha(char *str)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			i++;
		else if (str[i] >= 'A' && str[i] <= 'Z')
			i++;
		else
		{
			n = 0;
			i++;
		}
	}
	return (n);
}
